// exponensial -> oprasi perpangkatan
// Math.pow(base,exponent)
// saat ini disediakan opdasi (**)
// (^)
let result = Math.pow(2, 2);
console.log(result);
result = Math.pow(2, 4);
console.log(result);

// x**n
// x base number
// (**) oprasi exponensial
// n exmponent

let hasil = 2 ** 2;
console.log(hasil);
hasil = 2 ** 4;
console.log(hasil);

hasil = 2n ** 3n;
console.log(hasil);

